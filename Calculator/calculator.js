function add(a, b) {
    return a+b;
}

function subtraction(a, b) {
    return a-b;
}

function multiplication(a, b) {
    return a*b;
}

function divide(a, b) {
    return a/b;
}

let a = 5;
let b = 3;
console.log("ADD:", add(a, b));
console.log("SUBTRACT: ", subtraction(a, b));
console.log("MULTIPLICATION:", multiplication(a, b));
console.log("DIVIDE:", divide(a, b));